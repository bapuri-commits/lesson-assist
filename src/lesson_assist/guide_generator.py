"""NotebookLM 가이드 프롬프트를 자동 생성한다."""
from __future__ import annotations

from loguru import logger

from .config import AppConfig


def generate_guide(course: str, target_date: str, cfg: AppConfig) -> str:
    """과목/날짜별 NotebookLM 가이드 마크다운을 생성한다."""

    course_cfg = cfg.get_course_config(course)
    extra = course_cfg.guide_extra

    extra_section = ""
    if extra:
        extra_section = f"""
## 과목별 참고사항

{extra}
"""

    guide = f"""# {course} 학습 가이드 - {target_date}

이 문서는 NotebookLM이 업로드된 소스를 올바르게 이해하고 활용하도록 돕는 가이드입니다.

---

## 1. 소스 구성

이 노트북에는 다음 소스가 포함되어 있습니다:

| 소스 | 파일명 | 설명 |
|------|--------|------|
| 전사본 | 전사본_{target_date}.txt | 교수님 강의 음성을 텍스트로 변환한 기록. `[HH:MM:SS]` 형식의 타임스탬프 포함 |
| 수업자료 | PDF/PPT 파일들 | 해당 수업에서 사용된 강의 교안 원본 |
| 학습 컨텍스트 | 학습컨텍스트.md | 강의계획서, 현재 과제, 최근 공지 정리 (없을 수 있음) |

## 2. 소스 간 관계

### 전사본 ↔ 수업자료 매칭

교수님은 대부분 수업자료(PDF/PPT)의 내용을 **그대로 읽으면서** 중간중간 보충 설명을 추가하는 방식으로 강의합니다.
따라서 전사본과 수업자료를 연결할 때는 다음 두 가지 방법을 함께 사용하세요:

1. **원문 대조 (우선)**: 수업자료 PDF에 있는 실제 단어, 문구, 문장이 전사본에 유사하게 등장하면
   그 구간이 해당 페이지/슬라이드를 설명하는 부분입니다.
2. **키워드 감지 (보조)**: "이 슬라이드", "여기 보시면", "교재 N페이지" 등 명시적 언급도
   페이지 연결의 단서로 활용하세요.

### 기타 연결

- 전사본에서 과제/시험/마감을 언급하면
  -> **학습 컨텍스트의 과제 목록과 교차 확인**해주세요.
- 강의계획서의 주차별 토픽과 전사본의 실제 수업 내용을 비교하면
  -> 이번 수업이 커리큘럼에서 어디에 해당하는지 **진도 파악**이 가능합니다.

## 3. 주의사항

- **전사본 앞뒤의 비강의 내용**: 전사본의 시작과 끝 부분에는 강의와 관련 없는 내용
  (출석 확인, 잡담, 공지사항 전달, 수업 종료 인사 등)이 포함되어 있을 수 있습니다.
  이런 부분은 핵심 강의 내용과 구분하여 판단하세요.
  다만, 공지사항이나 과제 안내는 유용한 정보이므로 별도로 정리해주세요.
- **전사 오류**: 음성 인식 과정에서 전문 용어, 영어 용어, 고유명사가 부정확하게
  변환되었을 수 있습니다. 수업자료 PDF의 원문과 대조하여 올바른 용어로 교정해주세요.
- **구어체 표현**: 교수님의 발화는 구어체이므로 "그니까", "뭐냐면" 등의 표현은
  핵심 내용 정리 시 정제하되, 설명의 뉘앙스는 보존해주세요.

## 4. 활용 요청 가이드

### 강의 내용 정리

아래 구조로 정리해달라고 요청하면 체계적인 결과를 얻을 수 있습니다:

```
## 주제
(이번 수업의 핵심 주제 1줄)

## 핵심 개념
(새로 등장한 개념, 정의, 정리 - 정확한 정의 포함)

## 교수 강조 포인트
("시험에 나온다", "중요하다", "꼭 기억해라" 등 교수님이 강조한 부분)

## 예제와 직관
(교수님이 든 예시, 비유, 직관적 설명)

## 수업자료 참조
(전사본의 설명이 수업자료 몇 페이지/슬라이드와 대응하는지)

## 과제/공지 연관
(이번 수업과 관련된 과제, 공지, 일정)
```

### 시험 대비

- 핵심 암기 사항 (정의, 공식, 필요조건/충분조건)
- 이해 확인 질문 (스스로 개념을 점검할 수 있는 질문)
- 예상 문제 (교수 강조 포인트 + 수업자료 기반)
- 혼동 주의 (자주 헷갈리는 개념 비교)

### 결과 내보내기

학습이 끝나면, 아래와 같이 요청하여 정리된 결과를 복사할 수 있습니다:

**강의노트 요청**: "이번 강의 내용을 위의 구조대로 마크다운으로 정리해줘.
제목은 `# {course} - {target_date}` 로 해줘."

**학습노트 요청**: "이번 학습 내용을 기반으로 내가 꼭 알아야 할 것,
헷갈리는 것, 시험 대비 포인트를 정리해줘. 마크다운으로."
{extra_section}"""

    logger.info(f"NotebookLM 가이드 생성: {course} / {target_date}")
    return guide.strip() + "\n"
